def take_user_data():
    num_one = int(input("Enter First Number:"))
    num_two = int(input("Enter Second Number:"))
    return num_one, num_two


def add_me(num_one, num_two):
    return num_one + num_two


def sub_me(num_one, num_two):
    return num_one - num_two


def multiply_me(num_one, num_two):
    return num_one * num_two


def divide_me(num_one, num_two):
    return num_one / num_two
